# #19. How Do You Handle Exceptions With Try/Except/Finally In Python? Explain with coding snippets. 

# In Python, exceptions are handled using the try, except, else, and finally blocks. Each part of this structure serves a specific purpose:

# 1] try block: Contains the code that might raise an exception.

# 2] except block(s): Contains the code that handles exceptions. You can specify different types of exceptions to handle them differently.

# 3] else block: Executes if the try block does not raise an exception.

# 4] finally block: Executes regardless of whether an exception was raised or not, typically used for cleanup actions.