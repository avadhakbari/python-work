#18. What happens when „1‟== 1 is executed? 

# When the expression '1' == 1 is executed in Python, it evaluates to False. This is because the comparison operator == checks for equality
# of both value and type.


# Here’s why '1' == 1 is False:

#     Type Comparison: In this expression, '1' is a string (str type) and 1 is an integer (int type). Python requires that the types of 
#     the operands be the same to evaluate them as equal using ==.

#     Value Comparison: Even though '1' and 1 represent the same numeric value, Python does not automatically convert types for the 
#     == operator. The string '1' is not automatically converted to an integer 1 or vice versa.


