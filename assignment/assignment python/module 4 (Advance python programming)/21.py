# Object-Oriented Programming (OOP) is a paradigm that uses "objects" to design applications and programs. The key OOP concepts are:

# 1] Encapsulation: Wrapping the data (variables) and code (methods) together as a single unit, and restricting access to some of
# the object's components. This is typically done using access modifiers (private, protected, public).

# 2] Inheritance: Mechanism where a new class (child class) inherits properties and behavior (fields and methods) from an existing 
# class (parent class). This promotes code reuse.

# 3] Polymorphism: Ability to take many forms. It allows one interface to be used for a general class of actions. The specific action 
# is determined by the exact nature of the situation. It can be achieved via method overriding and method overloading.

# 4] Abstraction: Hiding the implementation details and showing only the functionality. It can be achieved using abstract classes and 
# interfaces.

