# A try-except block in Python can have multiple except clauses to handle different exceptions. There is no strict limit to the number 
# of except clauses you can have; you can include as many as necessary to handle different types of exceptions appropriately.

# >>> Some Built-in Exception Classes in Python:


# 1] ArithmeticError - Base class for all errors related to arithmetic operations.

#         ZeroDivisionError - Raised when the second argument of a division or modulo operation is zero.
#         OverflowError - Raised when the result of an arithmetic operation is too large to be represented.
#         FloatingPointError - Raised when a floating-point operation fails.

# 2] AttributeError - Raised when an attribute reference or assignment fails.

# 3] EOFError - Raised when the input() function hits an end-of-file condition (EOF) without reading any data.

# 4] ImportError - Raised when an import statement fails to import a module.

#         ModuleNotFoundError - A subclass of ImportError that is raised by import when a module could not be found.

# 5] IndexError - Raised when a sequence subscript is out of range.

# 6] KeyError - Raised when a dictionary key is not found.

# 7] KeyboardInterrupt - Raised when the user hits the interrupt key (usually Control-C or Delete).

# 8] MemoryError - Raised when an operation runs out of memory.

# 9] NameError - Raised when a local or global name is not found.

#         UnboundLocalError - A subclass of NameError raised when trying to access a local variable that has not been set.

# 10] OSError - Raised when a system function returns a system-related error, including I/O failures.

#         FileNotFoundError - Raised when a file or directory is requested but doesn’t exist.
#         PermissionError - Raised when trying to run an operation without the adequate access rights.

# 11] RuntimeError - Raised when an error is detected that doesn’t fall in any of the other categories.

#         RecursionError - Raised when the maximum recursion depth is exceeded.

# 12] SyntaxError - Raised when the parser encounters a syntax error.

#         IndentationError - Raised when there is incorrect indentation.
#             TabError - Raised when the indentation consists of inconsistent tabs and spaces.

# 13] TypeError - Raised when an operation or function is applied to an object of inappropriate type.

# 14] ValueError - Raised when a function gets an argument of the right type but inappropriate value.

# 15] StopIteration - Raised by next() function to indicate that there are no further items produced by the iterator.


